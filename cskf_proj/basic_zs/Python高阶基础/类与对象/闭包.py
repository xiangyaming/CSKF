# 闭包条件：   三个条件缺一不可
# 1.函数嵌套函数
# 2.外层函数返回嵌套函数的函数名
# 3.内层函数有引用到外层函数的一个非全局变量
# 作用：锁定数据，提高稳定性
def wai():
    num = 1
    print('外层')

    def nei():
        print(num)
        print('内层')
    return nei

